package ba.bild.code;

public class Calculator {

	double prvi;
	double drugi;

	public double sabiranje(double prvi, double drugi) {
		
		return prvi+drugi;
	}

	public double oduzimanje(double prvi, double drugi) {
		
		return prvi-drugi;
	}
	
	public double mnozenje(double prvi, double drugi) {
		return prvi*drugi;
	}
	
	public double dijeljenje(double prvi, double drugi) {
		return prvi/drugi;
	}
}
